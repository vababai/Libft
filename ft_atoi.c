#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				sign;
	long long int	res;

	i = 0;
	sign = 1;
	res = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (long long int)(str[i] - '0');
		i++;
	}
	if (res < 0 && sign > 0)
		return (-1);
	if (res < 0 && sign < 0)
		return (0);
	return (res * sign);
}
